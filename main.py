class InputExample(object):
    """
    A single training/test example for simple sequence classification.
    Args:
        guid: Unique id for the example.
        text_a: string. The untokenized text of the first sequence. For single
        sequence tasks, only this sequence must be specified.
        text_b: (Optional) string. The untokenized text of the second sequence.
        Only must be specified for sequence pair tasks.
        label: (Optional) string. The label of the example. This should be
        specified for train and dev examples, but not for test examples.
    """
    def __init__(self, guid, text_a, text_b=None, labels=None):
        self.guid = guid
        self.text_a = text_a
        self.text_b = text_b
        self.labels = labels

    def __repr__(self):
        return str(self.to_json_string())

    def to_dict(self):
        """Serializes this instance to a Python dictionary."""
        output = copy.deepcopy(self.__dict__)
        return output

    def to_json_string(self):
        """Serializes this instance to a JSON string."""
        return json.dumps(self.to_dict(), indent=2, sort_keys=True) + "\n"


class InputFeatures(object):
    """
    A single set of features of data.
    Args:
        input_ids: Indices of input sequence tokens in the vocabulary.
        attention_mask: Mask to avoid performing attention on padding token indices.
            Mask values selected in ``[0, 1]``:
            Usually  ``1`` for tokens that are NOT MASKED, ``0`` for MASKED (padded) tokens.
        token_type_ids: Segment token indices to indicate first and second portions of the inputs.
        label: Label corresponding to the input
    """

    def __init__(self, input_ids, attention_mask, token_type_ids, labels):
        self.input_ids = input_ids
        self.attention_mask = attention_mask
        self.token_type_ids = token_type_ids
        self.labels = labels
        
class MultiLabelTextProcessor(DataProcessor):

    def get_train_examples(self, data_dir):
        """See base class."""
        return self._create_examples(
            self._read_tsv(os.path.join(data_dir, "train.tsv")), "train")

    def get_dev_examples(self, data_dir):
        """See base class."""
        return self._create_examples(
            self._read_tsv(os.path.join(data_dir, "dev.tsv")), "dev")

    def get_labels(self):
        """See base class."""
        return ["1","2","3","4","5","6","7","8","9"]
    
    # get the inverse normed frequencies of the training set
    def get_inverse_normed_freqs(self, data_dir):
        lines = self._read_tsv(os.path.join(data_dir, "train.tsv"))
        freqs = [0] * 9
        for line in lines:
            labels = line[3:]
            for (i,label) in enumerate(labels):
                if float(label):
                    freqs[i] = freqs[i]+1
        inverse_freqs = [1./f for f in freqs]
        total_inverse_freqs = sum(inverse_freqs)
        inverse_normed_freqs = [i/total_inverse_freqs for i in inverse_freqs]
        return torch.FloatTensor(inverse_normed_freqs)
    
    # get the normed frequencies of the training set which will serve as weights_0 for focal3
    def get_normed_freqs(self, data_dir):
        lines = self._read_tsv(os.path.join(data_dir, "train.tsv"))
        freqs = [0] * 9
        for k,line in enumerate(lines):
            labels = line[3:]
            for (i,label) in enumerate(labels):
                if float(label):
                    freqs[i] = freqs[i]+1
        total_labels = (k+1) * (i+1) #train size * 9
#         print("total_labels ",total_labels)
#         print("freqs ", freqs)
        normed_freqs = [f/total_labels for f in freqs]
#         print("normed_freqs ",normed_freqs)
        return torch.FloatTensor(normed_freqs)
    


    def _create_examples(self, lines, set_type):
        """Creates examples for the training and dev sets."""
        examples = []
        for (i, line) in enumerate(lines):
            guid = "%s-%s" % (set_type, i)
#             logger.info("i:%d, %s" % (i,line))
            text_a = line[1]
            labels = line[3:]
            examples.append(
                InputExample(guid=guid, text_a=text_a, text_b=None, labels=labels))
        return examples

